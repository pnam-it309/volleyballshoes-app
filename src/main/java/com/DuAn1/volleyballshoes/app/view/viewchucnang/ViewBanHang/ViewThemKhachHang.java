    // Callback interface để truyền dữ liệu về ViewBanHang
    public interface ViewBanHangCallback {
        void onCustomerSelected(String tenKH, String sdt, String email);
    }
    
    private ViewBanHangCallback viewBanHangCallback;
    
    // Phương thức để thiết lập callback
    public void setViewBanHangCallback(ViewBanHangCallback callback) {
        this.viewBanHangCallback = callback;
    }
    
    // Phương thức thêm mới khách hàng
    private void themMoiKhachHang() {
        String tenKH = txtTenKH.getText().trim();
        String sdt = txtSDT.getText().trim();
        String email = jTextField1.getText().trim();
        String code = jTextField2.getText().trim();
        
        // Validate dữ liệu
        if (tenKH.isEmpty() || sdt.isEmpty()) {
            return;
        }
        
        try {
            // Tạo đối tượng khách hàng mới
            Customer newCustomer = new Customer();
            newCustomer.setCustomerFullName(tenKH);
            newCustomer.setCustomerPhone(sdt);
            newCustomer.setCustomerEmail(email.isEmpty() ? null : email);
            newCustomer.setCustomerCode(code.isEmpty() ? null : code);
            
            // Lưu vào database
            Customer createdCustomer = customerDAO.create(newCustomer);
            
            if (createdCustomer != null) {
                // Làm mới dữ liệu bảng
                loadCustomerData();
                
                // Xóa dữ liệu form
                txtTenKH.setText("");
                txtSDT.setText("");
                jTextField1.setText("");
                jTextField2.setText("");
                
                // Chuyển về tab danh sách
                jTabbedPane1.setSelectedIndex(0);
            }
        } catch (Exception e) {
            e.printStackTrace();
            // Xử lý lỗi nếu cần
        }
    }
    

    
